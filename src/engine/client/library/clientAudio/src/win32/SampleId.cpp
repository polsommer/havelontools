// ============================================================================

//-----------------------------------------------------------------------------
SampleId::SampleId()
 : m_id(0)
{
}

//-----------------------------------------------------------------------------
SampleId::SampleId(int const id)
 : m_id(id)
{
}

//-----------------------------------------------------------------------------
int SampleId::getId() const
{
	return m_id;
}

//-----------------------------------------------------------------------------
void SampleId::invalidate()
{
	m_id = 0;
}

//-----------------------------------------------------------------------------
bool SampleId::isValid() const
{
	return (m_id > 0);
}

